const colorSenal = "amarillo"

if(colorSenal == "rojo"){
    console.log("Es una señal reglamentaria")
}else if(colorSenal == "amarillo"){
    console.log("Es una señal preventiva")
}else if(colorSenal == "azul"){
    console.log("Es una señal informativa")
}else if(colorSenal == "naranja"){
    console.log("Es una señal provicional")
}else{
    console.log("Color de señal no válido")
}